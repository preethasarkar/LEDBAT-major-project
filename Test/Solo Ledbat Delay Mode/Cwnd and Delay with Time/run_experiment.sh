#!/bin/sh

BW=$1
RTT=$2
CC=$3

if [ -z "$BW" ] || [ -z "$RTT" ] || [ -z "$CC" ]; then
    echo "Usage: $0 <BW> <RTT> <CC>"
    echo "Example: $0 20Mbit/s 50ms ledbatpp"
    exit 1
fi

echo "--- 0. Pre-flight Cleanup ---"
# CRITICAL: Kill iperf3 FIRST. 
# This ensures that when setup_experiment calls cc_loader.sh, 
# the kernel module is not "in use" and can be safely unloaded/rebuilt.
killall iperf3 2>/dev/null
sleep 1 

echo "--- 1. Running setup script (Building & Loading Module) ---"
# We call setup_experiment.sh first because it contains your cc_loader.sh logic.
# This builds the .ko, unloads the old one, and loads the new one.
cd ../../
# This calls the setup script we modified earlier
./setup_experiment.sh 1 "$CC" "$BW" 500 "$RTT" || exit 1
cd - > /dev/null

echo "--- 2. Jail Configuration ---"
# Ensure the client's loopback is active
jexec client1 ifconfig lo0 127.0.0.1/8 up

# Client-side firewall rules
jexec client1 ipfw -q flush
jexec client1 ipfw add 65534 allow ip from any to any

# Host-side firewall rules 
ipfw add 1000 allow ip from any to any

echo "--- 4. Connectivity test ---"
jexec client1 ping -c 1 10.0.0.1 >/dev/null 2>&1
if [ $? -ne 0 ]; then
    echo "ERROR: client1 cannot reach Host (10.0.0.1)"
    exit 1
fi
echo "Connectivity OK ($CC)"

echo "--- 5. Start experiment ---"
# Clear kernel logs and stale route cache for fresh RTT measurements
dmesg -c > /dev/null
route flush > /dev/null 2>&1

# Start iperf server on the host
iperf3 -s -p 5201 -D

echo "Running $CC flow for 300 seconds..."
# Client1 connects to the host
jexec client1 iperf3 -c 10.0.0.1 -t 30 -p 5201 > throughput.txt

echo "--- 6. Extract CWND logs ---"
# Extract the TRACE data generated by your C code
echo "--- 6. Extract CWND logs ---"
TRACE_TAG="$(echo "$CC" | tr '[:lower:]' '[:upper:]')_TRACE"
dmesg | grep "^${TRACE_TAG}," | awk -F "${TRACE_TAG}," '{print $2}' > cwnd.csv

echo "Done. Graph generated: ${CC}_${BW}_${RTT}.png"

